public class ConstructorExample {

    public static void main(String[] args) {

    }

    String name;
    int Age;
    String schoolName;

    public ConstructorExample(String n, int a, String sName) {
        name = n;
        Age = a;
        schoolName = sName;

    }


    public String toString() {
        return ("Hello" + "" + name + "" + "Age:" + "" + Age + "" + "schoolName:" + "" + schoolName);


    }


}















